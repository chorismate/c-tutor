#!/usr/bin/false
# Configuration for c-tutor

# What C compiler? `cc` is normally redirected to the system C compiler,
#                  but if you'd like to use a different one, you can set it here.
c_compiler=cc

# What text editor? `pico` is considered very beginner-friendly and
#                   should be used by command-line newbies.
#                   `vim` is great if you're already familiar
#                   with the command line, but it has a learning curve.
text_editor=pico
